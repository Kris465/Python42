def is_safe_move(a, b, e, f, c, d):
    # Проверим, что фигура может попасть на (e, f) за один ход
    if (a == e and abs(b - f) == 1) or (b == f and abs(a - e) == 1):  # Ход по горизонтали или вертикали на 1 клетку
        return True
    elif abs(a - e) == 1 and abs(b - f) == 1:  # Ход по диагонали
        return True
    
    # Проверяем, что черная фигура не угрожает белой
    # Например, черная фигура атакует белую, если она находится в пределах 1 клетки от белой.
    if abs(c - e) <= 1 and abs(d - f) <= 1:  # Черная фигура угрожает клетке (e, f)
        return False
    
    return True


a, b = 4, 4  
e, f = 5, 5  
c, d = 6, 6  

if is_safe_move(a, b, e, f, c, d):
    print("Белая фигура может переместиться на поле (e, f), не попав под удар.")
else:
    print("Белая фигура не может переместиться на поле (e, f), так как она попадет под удар.")