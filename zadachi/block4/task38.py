x = int(input("Введите число: "))
y = int(input("Введите число: "))

if x > 2 and x < 3:
    print(f"Число {x} находится в интервале от 2 до 3")
else:
    print(f"Число {x} не находится в интервале от 2 до 3")

if y > -4 and y < -2:
    print(f"Число {y} находится в интервале от -4 до -2")
else:
    print(f"Число {y} не находится в интервале от -4 до -2")