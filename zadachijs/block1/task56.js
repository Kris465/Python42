const prompt = require("prompt-sync")();
const v1 = prompt("Введите скорость первого автомобиля: ")
const v2 = prompt("Введите скорость второго автомобиля (меньше чем скорость первого автомобиля): ")

const s1 = v1 * 30
const s2 = v2 * 30

console.log(`Разница между автомобилями через 30 мин будет ${s1 - s2} км`)