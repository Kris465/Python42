'''
Метод Евклида

Метод Евклида — это один из самых древних и эффективных алгоритмов
для нахождения наибольшего общего делителя (НОД) двух целых чисел.
Этот метод основывается на свойствах делимости
и позволяет быстро находить НОД без необходимости перечисления всех делителей.

Принцип работы алгоритма

Основная идея метода заключается в том,
что НОД двух чисел a и b (где a > b) 
равен НОД числа b и остатка от деления a на b.
Это можно выразить следующим образом:

НОД(a, b) = НОД(b, a (mod b))

где a (mod b) — это остаток от деления a на b.

Процесс продолжается до тех пор, пока одно из чисел не станет равным нулю.
В этом случае НОД будет равен другому числу.

Пошаговый алгоритм

1. Инициализация: Начните с двух чисел a и b, где a ≥ b > 0.

2. Вычисление остатка: Найдите остаток от деления a на b (обозначим его как r).

3. Замена: Замените a на b, а b на r.

4. Повторение: Повторяйте шаги 2-3, пока b не станет равным 0.

5. Результат: Когда одно из чисел станет 0, другое число будет НОД.

Пример

Рассмотрим пример нахождения НОД чисел 48 и 18:

1. Начинаем с a = 48, b = 18.

2. Находим остаток: 48 (mod 18) = 12.

3. Заменяем: теперь a = 18, b = 12.

4. Находим остаток: 18 (mod 12) = 6.

5. Заменяем: теперь a = 12, b = 6.

6. Находим остаток: 12 (mod 6) = 0.

7. Заменяем: теперь a = 6, b = 0.

8. Поскольку b = 0, НОД равен 6.

'''


def gcd(a, b):
    if b == 0:
        return abs(a)
    else:
        return gcd(b, a % b)

num1 = int(input("Введите первое число: "))
num2 = int(input("Введите первое число: "))
result = gcd(num1, num2)
print(f"Наибольший общий делитель {num1} и {num2} равен {result}")
